def star():
    t.pensize(2)
    t.pencolor('red')
    t.fillcolor('yellow')
    t.begin_fill()
    for i in range(5):
        t.forward(30)
        t.right(144)
    t.end_fill()
    t.pensize(10)
    t.pencolor('red')
def curly_star(setheading, circle_x, circle_y):
    set_position(0, 10)
    t.setheading(setheading)
    t.circle(circle_x, circle_y)
    star()
def straight_star(setheading, forward):
    set_position(0, 10)
    t.setheading(setheading)
    t.forward(forward)
    star()
